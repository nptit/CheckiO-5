def checkio(maze_map):
    move = {(1, 0): "S", (-1, 0): "N", (0, -1): "W", (0, 1): "E"}
    direction = ((0, 1), (1, 0), (0, -1), (-1, 0))
    opened = set()
    closed = set()
    g = {(1, 1): 0}
    h = {(1, 1): 18}
    parent = {}
    opened.add((1, 1))
    while len(opened):
        p = min(opened, key=lambda x: g[x] + h[x])
        opened.remove(p)
        closed.add(p)
        if p == (10, 10):
            break
        for d in direction:
            dx = p[0] + d[0]
            dy = p[1] + d[1]
            np = (dx, dy)
            if maze_map[dx][dy] == 0 and np not in closed:
                if np not in opened or g[np] > g[p] + 1:
                    g[np] = g[p] + 1
                    h[np] = 20 - dx - dy
                    opened.add(np)
                    parent[np] = p

    ret = ""
    p = (10, 10)
    if p not in closed:
        return "No route!"

    while p in parent:
        pp = parent[p]
        ret += move.get((p[0] - pp[0], p[1] - pp[1]))
        p = pp
    return ret[::-1]

